<?php

namespace App\Http\Requests;

use Illuminate\Foundation\Http\FormRequest;

class ContactRequest extends FormRequest
{
    public function authorize(): bool
    {
        return true;
    }

    public function rules()
{
    return [
        'first_name' => 'required',
        'last_name' => 'required',
        'gender' => 'required',
        'email' => ['required','email','regex:/^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$/'],
        'tel1' => 'required_without_all:tel2,tel3',
        'tel2' => 'required_without_all:tel1,tel3',
        'tel3' => 'required_without_all:tel1,tel2',
        'address' => 'required',
        'category_id' => 'required',
        'detail' => 'required|max:120',
    ];
}

public function messages()
{
    return [
        'first_name.required' => '姓を入力してください',
        'last_name.required' => '名を入力してください',
        'gender.required' => '性別を選択してください',
        'email.required' => 'メールアドレスを入力してください',
        'email.email' => 'メールアドレスはメール形式で入力してください',
        'email.regex' => 'メールアドレスはメール形式で入力してください',
        'tel1.required_without_all' => '電話番号を入力してください',
        'tel2.required_without_all' => '電話番号を入力してください',
        'tel3.required_without_all' => '電話番号を入力してください',
        'address.required' => '住所を入力してください',
        'category_id.required' => 'お問い合わせの種類を選択してください',
        'detail.required' => 'お問い合わせ内容を入力してください',
        'detail.max' => 'お問い合わせ内容は120文字以内で入力してください',
    ];
}

}
